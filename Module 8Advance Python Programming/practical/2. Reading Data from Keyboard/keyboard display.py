# Get user input for a string, an integer, and a float
user_string = input("Please enter a string: ")
user_integer = int(input("Please enter an integer: "))
user_float = float(input("Please enter a float: "))

# Display the inputs
print(f"You entered the string: '{user_string}'")
print(f"You entered the integer: {user_integer}")
print(f"You entered the float: {user_float}")
